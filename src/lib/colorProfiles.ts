export function getColorProfile(id: string | undefined): ColorProfile {
  const profile = COLOR_PROFILES.find((p) => p.id === (id || DEFAULT_PROFILE_ID));
  return profile ?? COLOR_PROFILES[0];
}

export function getProfileVars(profileId: string | undefined, isDark: boolean): Record<string, string> {
  const profile = getColorProfile(profileId);
  return isDark ? profile.dark : profile.light;
}
